def tabela(inicio = 65, fim = 74, tamanho = 5):
    
    print('+', '-'*9, '+', '-'*6, '+', '-'*6, '+', '-'*6, '+', '-'*5,'+')
    print('\n')
    print('+', '-'*9, '+', '-'*6, '+', '-'*6, '+', '-'*6, '+', '-'*5,'+')
    print('|  Bin√°rio ', '|  Oct  ', '|  Hex  ', '|  Dec  ', '|  Chr  |')
    print('+', '-'*9, '+', '-'*6, '+', '-'*6, '+', '-'*6, '+', '-'*5,'+')
    print('| {0:08b}  |  {0:3o}   |  {0:3x}   |  {0:03}   |   {0:1c}   |'.format(inicio))

    
    for k in range(inicio + 1, fim + 1):
        print('| {0:08b}  |  {0:3o}   |  {0:3x}   |  {0:03}   |   {0:1c}   |'.format(k))

        if (k+1) % tamanho == 0:
            print('+', '-'*9, '+', '-'*6, '+', '-'*6, '+', '-'*6, '+', '-'*5,'+')
            print('\n')
            print('+', '-'*9, '+', '-'*6, '+', '-'*6, '+', '-'*6, '+', '-'*5,'+')
        
tabela()





